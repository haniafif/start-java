package exercicios.e05;

public class Main {
    public static void main(String[] args){
         //++ --

         int num1;
         num1 = 0;

         //adcionando

         while (num1 <= 10){
             System.out.println(num1);
             num1++;

         }

        System.out.println("----------");

        num1 = 10;

        //diminuindo

        while (num1 > 0){
            System.out.println(num1);
            num1--;
        }


    }
}
